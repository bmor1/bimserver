public class TestOutInterceptor extends AbstractSoapInterceptor {

	private final String username;
	private final String password;

	public TestOutInterceptor(String username, String password) {
		super(Phase.PRE_PROTOCOL);
		this.username = username;
		this.password = password;
	}

	@Override
	public void handleMessage(SoapMessage message) throws Fault {
		message.getHeaders().add(new Header(new QName("username"), username));
		message.getHeaders().add(new Header(new QName("password"), password));
	}
}